DROP DATABASE IF EXISTS expendio_bebidas;
CREATE DATABASE expendio_bebidas CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE expendio_bebidas;


CREATE TABLE sucursales (
    id INT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    telefono VARCHAR(20) NOT NULL,
    email VARCHAR(120) NOT NULL,
    calle VARCHAR(120) NOT NULL,
    numero VARCHAR(20) NOT NULL,
    colonia VARCHAR(80) NOT NULL,
    ciudad VARCHAR(80) NOT NULL,
    estado VARCHAR(80) NOT NULL,
    codigo_postal VARCHAR(10) NOT NULL,
    fecha_apertura DATE NOT NULL,
    activa TINYINT(1) NOT NULL
);


CREATE TABLE empleados (
    id INT PRIMARY KEY,
    sucursal_id INT NOT NULL,
    nombre VARCHAR(80) NOT NULL,
    apellido_paterno VARCHAR(80) NOT NULL,
    apellido_materno VARCHAR(80) NOT NULL,
    puesto VARCHAR(80) NOT NULL,
    telefono VARCHAR(20) NOT NULL,
    email VARCHAR(120) NOT NULL,
    fecha_ingreso DATE NOT NULL,
    salario DECIMAL(10,2) NOT NULL,
    turno VARCHAR(30) NOT NULL,
    estatus VARCHAR(20) NOT NULL,
    FOREIGN KEY (sucursal_id) REFERENCES sucursales(id)
);


CREATE TABLE clientes (
    id INT PRIMARY KEY,
    nombre VARCHAR(80) NOT NULL,
    apellido_paterno VARCHAR(80) NOT NULL,
    apellido_materno VARCHAR(80) NOT NULL,
    telefono VARCHAR(20) NOT NULL,
    email VARCHAR(120) NOT NULL,
    calle VARCHAR(120) NOT NULL,
    numero VARCHAR(20) NOT NULL,
    colonia VARCHAR(80) NOT NULL,
    ciudad VARCHAR(80) NOT NULL,
    fecha_registro DATE NOT NULL,
    puntos INT NOT NULL
);


CREATE TABLE proveedores (
    id INT PRIMARY KEY,
    nombre_comercial VARCHAR(120) NOT NULL,
    contacto_nombre VARCHAR(120) NOT NULL,
    telefono VARCHAR(20) NOT NULL,
    email VARCHAR(120) NOT NULL,
    calle VARCHAR(120) NOT NULL,
    numero VARCHAR(20) NOT NULL,
    colonia VARCHAR(80) NOT NULL,
    ciudad VARCHAR(80) NOT NULL,
    rfc VARCHAR(20) NOT NULL,
    dias_credito INT NOT NULL,
    estatus VARCHAR(20) NOT NULL
);


CREATE TABLE productos (
    id INT PRIMARY KEY,
    proveedor_id INT NOT NULL,
    categoria VARCHAR(80) NOT NULL,
    codigo_barras VARCHAR(30) NOT NULL,
    nombre VARCHAR(150) NOT NULL,
    descripcion VARCHAR(255) NOT NULL,
    marca VARCHAR(80) NOT NULL,
    unidad_medida VARCHAR(30) NOT NULL,
    costo DECIMAL(10,2) NOT NULL,
    precio DECIMAL(10,2) NOT NULL,
    stock INT NOT NULL,
    stock_minimo INT NOT NULL,
    fecha_caducidad DATE NULL,
    activo TINYINT(1) NOT NULL,
    FOREIGN KEY (proveedor_id) REFERENCES proveedores(id)
);


CREATE TABLE ventas (
    id INT PRIMARY KEY,
    sucursal_id INT NOT NULL,
    empleado_id INT NOT NULL,
    cliente_id INT NOT NULL,
    folio VARCHAR(30) NOT NULL,
    fecha_venta DATETIME NOT NULL,
    metodo_pago VARCHAR(40) NOT NULL,
    subtotal DECIMAL(10,2) NOT NULL,
    descuento DECIMAL(10,2) NOT NULL,
    impuesto DECIMAL(10,2) NOT NULL,
    total DECIMAL(10,2) NOT NULL,
    estatus VARCHAR(20) NOT NULL,
    FOREIGN KEY (sucursal_id) REFERENCES sucursales(id),
    FOREIGN KEY (empleado_id) REFERENCES empleados(id),
    FOREIGN KEY (cliente_id) REFERENCES clientes(id)
);


CREATE TABLE detalle_venta (
    id INT PRIMARY KEY,
    venta_id INT NOT NULL,
    producto_id INT NOT NULL,
    cantidad INT NOT NULL,
    precio_unitario DECIMAL(10,2) NOT NULL,
    descuento DECIMAL(10,2) NOT NULL,
    impuesto DECIMAL(10,2) NOT NULL,
    subtotal DECIMAL(10,2) NOT NULL,
    observaciones VARCHAR(150) NOT NULL,
    entregado TINYINT(1) NOT NULL,
    FOREIGN KEY (venta_id) REFERENCES ventas(id),
    FOREIGN KEY (producto_id) REFERENCES productos(id)
);

